import torch
import torch.nn as nn
from mypath import Path

class C3D(nn.Module):
    """
    The C3D network.
    """

    def __init__(self, num_classes, pretrained=False):
        super(C3D, self).__init__()

        self.conv1 = nn.Conv3d(3, 64, kernel_size=(3, 3, 3), padding=(1, 1, 1))
        self.pool1 = nn.MaxPool3d(kernel_size=(1, 2, 2), stride=(1, 2, 2))

        self.conv2 = nn.Conv3d(64, 128, kernel_size=(3, 3, 3), padding=(1, 1, 1))
        self.pool2 = nn.MaxPool3d(kernel_size=(2, 2, 2), stride=(2, 2, 2))

        self.conv3a = nn.Conv3d(128, 256, kernel_size=(3, 3, 3), padding=(1, 1, 1))
        self.conv3b = nn.Conv3d(256, 256, kernel_size=(3, 3, 3), padding=(1, 1, 1))
        self.pool3 = nn.MaxPool3d(kernel_size=(2, 2, 2), stride=(2, 2, 2))

        self.conv4a = nn.Conv3d(256, 512, kernel_size=(3, 3, 3), padding=(1, 1, 1))
        self.conv4b = nn.Conv3d(512, 512, kernel_size=(3, 3, 3), padding=(1, 1, 1))
        self.pool4 = nn.MaxPool3d(kernel_size=(2, 2, 2), stride=(2, 2, 2))

        self.conv5a = nn.Conv3d(512, 512, kernel_size=(3, 3, 3), padding=(1, 1, 1))
        self.conv5b = nn.Conv3d(512, 512, kernel_size=(3, 3, 3), padding=(1, 1, 1))
        self.pool5 = nn.MaxPool3d(kernel_size=(2, 2, 2), stride=(2, 2, 2), padding=(0, 1, 1))

        self.fc6 = nn.Linear(8192, 4096)
        # self.fc6 = nn.Linear(32256, 4096)
        self.fc7 = nn.Linear(4096, 4096)
        self.fc8 = nn.Linear(4096, num_classes)

        self.dropout = nn.Dropout(p=0.5)

        self.relu = nn.ReLU()

        self.__init_weight()

        if pretrained:
            self.__load_pretrained_weights()

    def forward(self, x):
        # print(x.shape)
        # a = x[0,:,0,:,:].cpu()
        # a = a.transpose(0,2)
        # a = a.transpose(0,1)

        # import matplotlib.pyplot as plt
        # plt.title('c3d model level img')
        # plt.imshow(a)
        # plt.show()

        # x = x.unsqueeze(1)      # 처음에 x는 (batch,20,512,512) 이다. 여기서 channel에 대한 차원이 없어서 batch와 frame수를 나타내는 차원 사이에 1차원을 추가한다.->(batch, 1, 20, 512, 512)
        # print('1 : ', torch.max(x))

        # a_max1 = torch.max(x[0,:,0,...])
        # a_max2 = torch.max(x[0,0,0,...])
        # a_max3 = torch.max(x[0,1,0,...])
        # a_mean1 = torch.mean(x[0,:,0,...])
        # a_mean2 = torch.mean(x[0,0,0,...])
        # a_mean3 = torch.mean(x[0,1,0,...])
        # print('max 1 : ',a_max1)
        # print('max 2 : ',a_max2)
        # print('max 3 : ',a_max3)
        # print('mean 1 : ', a_mean1)
        # print('mean 2 : ', a_mean2)
        # print('mean 3 : ', a_mean3)

        # x shape : torch.Size([4, 3, 20, 112, 112])
        x = self.relu(self.conv1(x))
        # print('2: ', torch.max(x))
        x = self.pool1(x)
        # print(torch.max(x))

        x = self.relu(self.conv2(x))
        x = self.pool2(x)

        x = self.relu(self.conv3a(x))
        x = self.relu(self.conv3b(x))
        x = self.pool3(x)

        x = self.relu(self.conv4a(x))
